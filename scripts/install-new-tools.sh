#!/usr/bin/env bash
# install-new-tools.sh - Symlink agents, skills, and commands from New Tools plugins
# into the active Claude Code directories.
#
# Source of truth: ~/.claude/New Tools/agents/.claude-plugin/marketplace.json
# Deduplicates by basename (first occurrence wins).
# Generates PLUGINS_INDEX.md manifests for agents and skills.

set -uo pipefail

CLAUDE_DIR="${HOME}/.claude"
NEW_TOOLS_DIR="${CLAUDE_DIR}/New Tools/agents/plugins"
MARKETPLACE="${CLAUDE_DIR}/New Tools/agents/.claude-plugin/marketplace.json"

AGENTS_DIR="${CLAUDE_DIR}/agents"
SKILLS_DIR="${CLAUDE_DIR}/skills"
COMMANDS_DIR="${CLAUDE_DIR}/commands/plugins"

# Counters
agent_count=0
skill_count=0
cmd_count=0
skip_count=0

log() { printf '%s\n' "$1"; }
warn() { printf 'WARN: %s\n' "$1" >&2; }

# Verify marketplace.json exists
if [[ ! -f "$MARKETPLACE" ]]; then
    echo "ERROR: marketplace.json not found at $MARKETPLACE" >&2
    exit 1
fi

# Create target directories
mkdir -p "$AGENTS_DIR" "$SKILLS_DIR" "$COMMANDS_DIR"

# ---- Agents ----
log "=== Symlinking Agents ==="

# Find all unique agent .md files across plugins (deduplicate by basename)
declare -A seen_agents
while IFS= read -r -d '' agent_file; do
    basename_file="$(basename "$agent_file")"

    # Skip non-agent files
    [[ "$basename_file" == "SKILL.md" ]] && continue
    [[ "$basename_file" == "README.md" ]] && continue

    # Deduplicate: first occurrence wins
    if [[ -n "${seen_agents[$basename_file]+x}" ]]; then
        ((skip_count++))
        continue
    fi
    seen_agents["$basename_file"]=1

    target="${AGENTS_DIR}/${basename_file}"

    # Skip if a real file (not symlink) already exists
    if [[ -f "$target" && ! -L "$target" ]]; then
        log "  SKIP (existing): $basename_file"
        ((skip_count++))
        continue
    fi

    # Create or update symlink
    ln -sf "$agent_file" "$target"
    ((agent_count++))
done < <(find "$NEW_TOOLS_DIR" -path "*/agents/*.md" -type f -print0 | sort -z)

log "  Agents linked: $agent_count (skipped: $skip_count)"

# ---- Skills ----
log "=== Symlinking Skills ==="
skip_count=0

declare -A seen_skills
while IFS= read -r -d '' skill_dir; do
    basename_dir="$(basename "$skill_dir")"

    # Deduplicate
    if [[ -n "${seen_skills[$basename_dir]+x}" ]]; then
        ((skip_count++))
        continue
    fi
    seen_skills["$basename_dir"]=1

    target="${SKILLS_DIR}/${basename_dir}"

    # Skip if a real directory (not symlink) already exists
    if [[ -d "$target" && ! -L "$target" ]]; then
        log "  SKIP (existing): $basename_dir"
        ((skip_count++))
        continue
    fi

    ln -sfn "$skill_dir" "$target"
    ((skill_count++))
done < <(find "$NEW_TOOLS_DIR" -path "*/skills/*" -maxdepth 3 -mindepth 3 -type d -print0 | sort -z)

log "  Skills linked: $skill_count (skipped: $skip_count)"

# ---- Commands ----
log "=== Symlinking Commands ==="
skip_count=0

declare -A seen_cmds
while IFS= read -r -d '' cmd_file; do
    basename_file="$(basename "$cmd_file")"

    # Deduplicate
    if [[ -n "${seen_cmds[$basename_file]+x}" ]]; then
        ((skip_count++))
        continue
    fi
    seen_cmds["$basename_file"]=1

    target="${COMMANDS_DIR}/${basename_file}"

    if [[ -f "$target" && ! -L "$target" ]]; then
        log "  SKIP (existing): $basename_file"
        ((skip_count++))
        continue
    fi

    ln -sf "$cmd_file" "$target"
    ((cmd_count++))
done < <(find "$NEW_TOOLS_DIR" -path "*/commands/*.md" -type f -print0 | sort -z)

log "  Commands linked: $cmd_count (skipped: $skip_count)"

# ---- Generate PLUGINS_INDEX.md for agents ----
log "=== Generating Agents Index ==="
{
    echo "# Plugin Agents Index"
    echo ""
    echo "Auto-generated by install-new-tools.sh from marketplace.json."
    echo "Source: \`~/.claude/New Tools/agents/.claude-plugin/marketplace.json\`"
    echo ""
    echo "| Agent | Source Plugin | Category |"
    echo "|-------|-------------|----------|"
} > "${AGENTS_DIR}/PLUGINS_INDEX.md"

# Parse marketplace.json with python for reliable JSON handling
python3 -c "
import json, os
with open('${MARKETPLACE}') as f:
    data = json.load(f)
seen = set()
for plugin in data['plugins']:
    category = plugin.get('category', 'unknown')
    for agent_path in plugin.get('agents', []):
        basename = os.path.basename(agent_path)
        if basename not in seen and basename not in ('SKILL.md', 'README.md'):
            seen.add(basename)
            name = basename.replace('.md', '')
            print(f'| {name} | {plugin[\"name\"]} | {category} |')
" >> "${AGENTS_DIR}/PLUGINS_INDEX.md"

# ---- Generate PLUGINS_INDEX.md for skills ----
log "=== Generating Skills Index ==="
{
    echo "# Plugin Skills Index"
    echo ""
    echo "Auto-generated by install-new-tools.sh from marketplace.json."
    echo "Source: \`~/.claude/New Tools/agents/.claude-plugin/marketplace.json\`"
    echo ""
    echo "| Skill | Source Plugin | Category |"
    echo "|-------|-------------|----------|"
} > "${SKILLS_DIR}/PLUGINS_INDEX.md"

python3 -c "
import json, os
with open('${MARKETPLACE}') as f:
    data = json.load(f)
seen = set()
for plugin in data['plugins']:
    category = plugin.get('category', 'unknown')
    for skill_path in plugin.get('skills', []):
        basename = os.path.basename(skill_path)
        if basename not in seen:
            seen.add(basename)
            print(f'| {basename} | {plugin[\"name\"]} | {category} |')
" >> "${SKILLS_DIR}/PLUGINS_INDEX.md"

# ---- Summary ----
echo ""
echo "========================================="
echo "  Installation Complete"
echo "========================================="
echo "  Agents:   $agent_count symlinks"
echo "  Skills:   $skill_count symlinks"
echo "  Commands: $cmd_count symlinks"
echo ""
echo "  Index files:"
echo "    ${AGENTS_DIR}/PLUGINS_INDEX.md"
echo "    ${SKILLS_DIR}/PLUGINS_INDEX.md"
echo "========================================="
