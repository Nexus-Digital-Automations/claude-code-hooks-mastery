def _needs_clarification(prompt: str) -> bool:
    """Detect vague app/tool tasks that need requirements gathered before starting."""
    prompt_lower = prompt.lower()
    # Must contain an app/tool keyword to even consider clarification
    if not any(kw in prompt_lower for kw in _APP_KEYWORDS):
        return False
    # Already has explicit backend or frontend tech signals — specified enough
    if _is_backend_task(prompt):
        return False
    if _EXPLICIT_FRONTEND.search(prompt_lower):
        return False
    return True


def _get_dir_tree(working_dir: str, max_depth: int = 3) -> str:
    """Generate a compact directory tree for context injection into Qwen tasks.

    Eliminates unnecessary list_dir API calls during delegation by providing
    the file structure upfront. Uses os.walk to avoid subprocess/find portability
    issues with brace-expansion in -name patterns.
    """
    import os

    EXCLUDE_DIRS = {
        'node_modules', '.git', 'dist', '__pycache__', '.next',
        'build', 'coverage', 'venv', '.venv', '.tox', 'target',
    }
    INCLUDE_EXTS = {'.js', '.jsx', '.ts', '.tsx', '.py', '.json', '.md'}

    try:
        files = []
        base = os.path.normpath(working_dir)
        for root, dirs, filenames in os.walk(base):
            # Prune excluded directories in-place so os.walk skips them
            dirs[:] = [d for d in dirs if d not in EXCLUDE_DIRS]
            depth = root[len(base):].count(os.sep)
            if depth >= max_depth:
                dirs.clear()
                continue
            for fname in filenames:
                ext = os.path.splitext(fname)[1].lower()
                if ext in INCLUDE_EXTS:
                    rel = os.path.relpath(os.path.join(root, fname), base)
                    files.append(rel)
        files.sort()
        if len(files) > 60:
            files = files[:60] + [f'... ({len(files) - 60} more)']
        return '\n'.join(files) if files else '(no matching files found)'
    except Exception:
        return '(directory tree unavailable)'


