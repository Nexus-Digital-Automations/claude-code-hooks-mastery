def _record_task_start(session_id: str, prompt: str, working_dir: str = "") -> tuple:
    """Write current_task.json with reliable task-start timestamp and task_id.

    Generates a unique task_id (UUID) for state isolation. If this is a follow-up
    prompt in the same session with non-pending VR checks, preserves the existing
    task_id to prevent authorize-stop → re-prompt → new task_id cycles (IMP-017).

    Also generates a unique prompt_id (UUID) for every prompt — never reused, even
    for follow-ups. This enables the Qwen reviewer to detect stale Q&A history
    from prior prompts and prevent cross-task contamination.

    A working_dir change within the same session forces a new task (even if VR
    has progress) to prevent cross-project contamination.

    Returns (task_id, prompt_id, is_followup) tuple.
    """
    # Session-scoped task file — prevents concurrent sessions from clobbering each other
    task_file = Path.home() / f".claude/data/current_task_{session_id}.json"
    existing_task_id = None
    is_followup = False
    existing_data = {}

    try:
        task_file.parent.mkdir(parents=True, exist_ok=True)

        # Check if this is a follow-up to an existing task
        if task_file.exists():
            try:
                existing_data = json.loads(task_file.read_text())
                if existing_data.get("session_id") == session_id:
                    # Project switch = new task, regardless of VR progress
                    old_wd = existing_data.get("working_dir", "")
                    if old_wd and working_dir and old_wd != working_dir:
                        pass  # is_followup stays False, existing_task_id stays None
                    else:
                        # Same project — check if VR has work in progress
                        # Use session_id (not task_id) — matches how all other
                        # code reads VR files (stop.py, check-*.sh, vr_utils.py)
                        vr_file = Path.home() / f".claude/data/verification_record_{session_id}.json"
                        if vr_file.exists():
                            vr = json.loads(vr_file.read_text())
                            checks = vr.get("checks", {})
                            has_progress = any(
                                c.get("status") != "pending"
                                for c in checks.values()
                                if isinstance(c, dict)
                            )
                            if has_progress:
                                existing_task_id = existing_data.get("task_id")
                                is_followup = True
            except Exception:
                pass

        task_id = existing_task_id or str(uuid.uuid4())
        # prompt_id is ALWAYS fresh — never reused, even for follow-ups.
        # This lets the Qwen reviewer detect stale Q&A from prior prompts.
        prompt_id = str(uuid.uuid4())

        # Read agent_id from session-scoped identity (generated by session_start.py)
        agent_id = ""
        try:
            _identity_file = Path.home() / f".claude/data/agent_identity_{session_id}.json"
            if not _identity_file.exists():
                _identity_file = Path.home() / ".claude/data/agent_identity.json"
            if _identity_file.exists():
                _identity = json.loads(_identity_file.read_text())
                agent_id = _identity.get("agent_id", "")
        except Exception:
            pass

        task_file.write_text(json.dumps({
            "task_id": task_id,
            "prompt_id": prompt_id,
            "agent_id": agent_id,
            "session_id": session_id,
            "working_dir": working_dir,
            "task_started_at": (
                existing_data.get("task_started_at")
                if is_followup
                else datetime.now().isoformat()
            ),
            "prompt": prompt[:500],
            "is_followup": is_followup,
        }, indent=2))
        return task_id, prompt_id, is_followup
    except Exception:
        return str(uuid.uuid4()), str(uuid.uuid4()), False  # Never block prompt submission


def _try_inherit_previous_vr(session_id: str, task_id: str, working_dir: str) -> bool:
    """Inherit VR state from the most recent previous session for the same project.

    Handles interrupted sessions: when Claude restarts with a new session_id for
    the same working_dir, completed checks from the interrupted session are preserved
    so the user doesn't have to re-run everything.

    Returns True if a previous VR with completed checks was found and copied.
    """
    try:
        import time
        data_dir = Path.home() / ".claude/data"
        best_sid = None
        best_mtime = 0.0

        for task_path in data_dir.glob("current_task_*.json"):
            # Skip the current session's task file
            if session_id in task_path.name:
                continue
            try:
                mtime = task_path.stat().st_mtime
                if mtime < time.time() - 24 * 3600:  # Ignore files older than 24h
                    continue
                data = json.loads(task_path.read_text())
                if data.get("working_dir") == working_dir:
                    if mtime > best_mtime:
                        best_mtime = mtime
                        best_sid = data.get("session_id")
            except Exception:
                continue

        if not best_sid:
            return False

        old_vr_file = data_dir / f"verification_record_{best_sid}.json"
        if not old_vr_file.exists():
            return False

        old_record = json.loads(old_vr_file.read_text())
        old_checks = old_record.get("checks", {})

        # Only inherit if there are non-pending checks worth preserving
        if not any(
            isinstance(c, dict) and c.get("status") not in ("pending", None)
            for c in old_checks.values()
        ):
            return False

        # Copy old VR into the new session (updating session_id/task_id)
        new_vr_file = data_dir / f"verification_record_{session_id}.json"
        new_record = {
            "reset_at": datetime.now().isoformat(),
            "session_id": session_id,
            "task_id": task_id,
            "inherited_from": best_sid,
            "checks": old_checks,
        }
        new_vr_file.write_text(json.dumps(new_record, indent=2))
        return True
    except Exception:
        return False  # Never block prompt submission


def _reset_verification_for_new_task(session_id: str, task_id: str,
                                     is_followup: bool,
                                     working_dir: str = "") -> None:
    """Reset verification state at start of each new user prompt.

    Prevents stale evidence from a previous task in the same session
    from being used as proof for the current task's work.
    Mirrors session_start.py:reset_verification_record() but runs per-prompt.

    If is_followup is True (same session, VR has non-pending checks), skips
    the VR reset to preserve check evidence across authorize-stop retry
    cycles (IMP-017). Qwen review state is ALWAYS cleaned regardless
    of follow-up status to prevent cross-task contamination.
    """
    # Qwen state cleanup consolidated to session_start.py.
    # authorize-stop.sh clears per-agent state after each authorization.

    # ── VR reset — guarded by follow-up check (IMP-017) ──
    if is_followup:
        return  # Preserve existing VR state for follow-up prompts

    # Cross-session continuity: inherit completed checks from a previous session
    # for the same project. Handles interrupted sessions (Ctrl+C restarts).
    if working_dir and _try_inherit_previous_vr(session_id, task_id, working_dir):
        return

    # Use session_id — matches how stop.py, check-*.sh, and authorize-stop.sh read VR files.
    # Previous bug: used task_id here, creating ghost files nobody reads.
    vr_file = Path.home() / f".claude/data/verification_record_{session_id}.json"
    try:
        vr_file.parent.mkdir(parents=True, exist_ok=True)
        # Canonical check keys matching VR_CHECKS_ORDER in vr_utils.py
        all_pending = {
            k: {"status": "pending", "evidence": None, "timestamp": None,
                "skip_reason": None}
            for k in [
                "tests", "build", "lint", "typecheck", "app_starts",
                "execution", "frontend", "happy_path", "security",
                "commit_push", "upstream_sync",
            ]
        }
        with open(vr_file, "w") as f:
            json.dump({
                "reset_at": datetime.now().isoformat(),
                "session_id": session_id,
                "task_id": task_id,
                "checks": all_pending,
            }, f, indent=2)
    except Exception:
        pass  # Never block prompt submission


